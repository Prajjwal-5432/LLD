package DesignPatterns.Behavioural.Observer;

public class Order {
    private long amount;
    private long id;

    public long getAmount() {
        return amount;
    }

    public void setAmount(long amount) {
        this.amount = amount;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }
}
